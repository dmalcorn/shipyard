        git log --oneline -1
        echo ""
        return 0
    else
        log "ERROR" "Failed to create commit"
        save_state "git" "failed"
        return 1
    fi
}

# =============================================================================
# Check Dependencies — install missing Python packages automatically
# =============================================================================
check_dependencies() {
    local missing=false

    # --- Required system commands ---
    if ! command -v claude &> /dev/null; then
        log "ERROR" "Claude CLI not found. Install: npm install -g @anthropic-ai/claude-code"
        missing=true
    else
        log "INFO" "claude: $(claude --version 2>&1 | head -1)"
    fi

    if ! command -v git &> /dev/null; then
        log "ERROR" "git not found. Install git first."
        missing=true
    else
        log "INFO" "git: $(git --version 2>&1)"
    fi

    if ! command -v python &> /dev/null; then
        log "ERROR" "python not found. Install Python 3.13+."
        missing=true
    else
        log "INFO" "python: $(python --version 2>&1)"
    fi

    if [[ "$missing" == "true" ]]; then
        log "ERROR" "Missing required system dependencies — cannot continue."
        exit 1
    fi

    # --- Required Python packages (auto-install from requirements-dev.txt) ---
    local py_packages=("ruff" "mypy" "pytest")
    local need_install=false

    for pkg in "${py_packages[@]}"; do
        if ! python -m "$pkg" --version &> /dev/null; then
            log "WARN" "$pkg not found in Python environment."
            need_install=true
        else
            log "INFO" "$pkg: $(python -m "$pkg" --version 2>&1 | head -1)"
        fi
    done

    if [[ "$need_install" == "true" ]]; then
        local req_file="$PROJECT_ROOT/requirements-dev.txt"
        if [[ -f "$req_file" ]]; then
            log "INFO" "Installing missing Python packages from requirements-dev.txt..."
            if python -m pip install -r "$req_file" --quiet 2>&1; then
                log "SUCCESS" "Python packages installed successfully."
                # Verify after install
                for pkg in "${py_packages[@]}"; do
                    if ! python -m "$pkg" --version &> /dev/null; then
                        log "ERROR" "$pkg still not available after install."
                        missing=true
                    else
                        log "INFO" "$pkg: $(python -m "$pkg" --version 2>&1 | head -1)"
                    fi
                done
            else
                log "ERROR" "pip install failed. Run manually: pip install -r $req_file"
                missing=true
            fi
        else
            log "ERROR" "requirements-dev.txt not found at $req_file — install ruff, mypy, pytest manually."
            missing=true
        fi
    fi

    if [[ "$missing" == "true" ]]; then
        log "ERROR" "Missing Python dependencies — cannot continue."
        exit 1
    fi
}

