# PHASE 5: Run CI Pipeline with Retry Loop
# =============================================================================
run_ci_phase() {
    local phase_start=$(date +%s)

    echo ""
    echo -e "${MAGENTA}============================================================${NC}"
    log "PHASE" "PHASE 5: CI PIPELINE"
    echo -e "${MAGENTA}============================================================${NC}"
    echo ""

    save_state "ci" "in_progress"

    local ci_script="$PROJECT_ROOT/scripts/local_ci.sh"

    if [[ ! -f "$ci_script" ]]; then
        log "ERROR" "CI script not found: $ci_script"
        return 1
    fi

    chmod +x "$ci_script" 2>/dev/null || true

    log "INFO" "Running local CI pipeline ($ci_script)"
    log "INFO" "Max attempts: $MAX_CI_ATTEMPTS"

    cd "$PROJECT_ROOT"

    local ci_passed=false
    local ci_attempt=0
    local ci_timeout_retries=0
    local max_ci_timeout_retries=1

    while [[ $ci_attempt -lt $MAX_CI_ATTEMPTS && "$ci_passed" != "true" ]]; do
        ((ci_attempt++))
        save_state "ci" "attempt_$ci_attempt"
        log "INFO" "CI attempt $ci_attempt of $MAX_CI_ATTEMPTS"

        local ci_result=""
        local ci_exit_code=0

        ci_result=$("$ci_script" 2>&1) || ci_exit_code=$?

        echo "$ci_result" >> "$LOG_FILE"

        if [[ $ci_exit_code -eq 0 ]]; then
            ci_passed=true
            local phase_duration=$(format_duration $(($(date +%s) - phase_start)))
            log "SUCCESS" "CI passed! ($phase_duration)"
        else
            log "WARN" "CI failed (exit code: $ci_exit_code), asking Claude to fix..."

            echo ""
            echo -e "${RED}=== CI FAILURE SUMMARY ===${NC}"

            # Extract test failures
            local test_failures=$(echo "$ci_result" | grep -E "^FAILED " | head -10)
            if [[ -n "$test_failures" ]]; then
                echo -e "${YELLOW}Test Failures:${NC}"
                echo "$test_failures"
                echo ""
            fi

            # Extract lint errors
            local ruff_errors=$(echo "$ci_result" | grep -E "^(src|tests)/.*:[0-9]+:[0-9]+:" | head -10)
            if [[ -n "$ruff_errors" ]]; then
                echo -e "${YELLOW}Lint Errors (ruff):${NC}"
                echo "$ruff_errors"
                echo ""
            fi

            # Extract type errors
            local mypy_errors=$(echo "$ci_result" | grep -E "^(src|tests)/.*: error:" | head -10)
            if [[ -n "$mypy_errors" ]]; then
                echo -e "${YELLOW}Type Errors (mypy):${NC}"
                echo "$mypy_errors"
                echo ""
            fi

            echo -e "${RED}=== END FAILURE SUMMARY ===${NC}"
            echo ""

            local ci_issues_file="$LOOPER_DIR/epic${EPIC_NUMBER}-ci-issues-attempt-${ci_attempt}.txt"
            echo "$ci_result" > "$ci_issues_file"

            local fix_prompt="Fix CI failures for Epic $EPIC_NUMBER in the Shipyard project.

CRITICAL FIRST STEP: Read $PROJECT_ROOT/CLAUDE.md and $PROJECT_ROOT/coding-standards.md for coding rules.

CI ISSUES FILE: $ci_issues_file

Fix all lint errors (ruff), type errors (mypy), and test failures (pytest).

After fixing, output:
=== CI FIXES APPLIED ===
- [LINT/TEST/TYPE] Brief description (file:line)
=== END CI FIXES ===

Begin by reading CLAUDE.md and the CI issues file now."

            local timestamp=$(get_timestamp)
            cat >> "$LOG_FILE" << EOF

================================================================================
PROMPT: Phase 5 CI Fix | Attempt $ci_attempt | Time: $timestamp
================================================================================
$fix_prompt
================================================================================

EOF

            log "INFO" "Pausing 15 seconds before CI fix..."
            sleep 15

            echo -e "${YELLOW}>>> Invoking Dev Agent to fix CI issues...${NC}"

            run_claude_with_signal_handling "$fix_prompt" "$TOOLS_CI_FIX" "$CLAUDE_TIMEOUT_LONG"
            local fix_result="$CLAUDE_RESULT"
            local fix_exit_code=$CLAUDE_EXIT_CODE

            cat >> "$LOG_FILE" << EOF

================================================================================
OUTPUT: Phase 5 CI Fix | Attempt $ci_attempt
================================================================================
$fix_result
================================================================================

EOF

            log_output_preview "CI Fix Attempt $ci_attempt" "$fix_result" 15

            # Handle timeout
            if [[ $fix_exit_code -eq 124 ]]; then
                ((ci_timeout_retries++))
                log "WARN" "CI fix timed out after $CLAUDE_TIMEOUT_LONG"
                if [[ $ci_timeout_retries -le $max_ci_timeout_retries ]]; then
                    ((ci_attempt--))
                    sleep 30
                    continue
                else
                    log "ERROR" "CI fix timed out twice - stopping"
                    save_state "ci" "timeout_failed"
                    return 1
                fi
            fi
        fi
    done

    # Verification run if needed
    if [[ "$ci_passed" != "true" && $ci_attempt -eq $MAX_CI_ATTEMPTS ]]; then
        log "INFO" "Running verification CI after final fix attempt..."
        ci_exit_code=0
        ci_result=$("$ci_script" 2>&1) || ci_exit_code=$?
        if [[ $ci_exit_code -eq 0 ]]; then
            ci_passed=true
            log "SUCCESS" "CI passed on verification run!"
        fi
    fi

    if [[ "$ci_passed" != "true" ]]; then
        local phase_duration=$(format_duration $(($(date +%s) - phase_start)))
        log "ERROR" "CI failed after $MAX_CI_ATTEMPTS attempts ($phase_duration)"
        save_state "ci" "failed"
        return 1
    fi

    local phase_duration=$(format_duration $(($(date +%s) - phase_start)))
    log "SUCCESS" "Phase 5 (CI) complete - Duration: $phase_duration"
    save_state "ci" "complete"

    return 0
}

# =============================================================================
# Git Commit with Pre-commit Hook Retry Logic
# =============================================================================
git_commit_changes() {
    local commit_msg="$1"
    local max_retries=3
    local retry_count=0

    cd "$PROJECT_ROOT"

    # Pre-commit: basic ruff check
    log "INFO" "Running pre-commit ruff check..."
    if command -v ruff &> /dev/null; then
        ruff check "$PROJECT_ROOT/src" "$PROJECT_ROOT/tests" --select=E,F --ignore=E501 --fix --quiet 2>&1 || true
    fi

    while [[ $retry_count -lt $max_retries ]]; do
        git add .

        local has_changes=$(git status --porcelain)
        if [[ -z "$has_changes" ]]; then
            log "INFO" "No changes to commit"
            return 0
        fi

        local commit_output
        local exit_code=0

        commit_output=$(git commit -m "$commit_msg" 2>&1) || exit_code=$?

        if [[ $retry_count -eq 0 ]]; then
            echo "$commit_output" | tee -a "$LOG_FILE"
        else
            echo "$commit_output" >> "$LOG_FILE"
        fi

        if [[ $exit_code -eq 0 ]]; then
            if [[ $retry_count -gt 0 ]]; then
                log "SUCCESS" "Commit succeeded after pre-commit auto-fixes (attempt $((retry_count + 1)))"
            else
                log "SUCCESS" "Created commit: $commit_msg"
            fi
            return 0
        fi

        # Check if pre-commit hooks modified files
        if echo "$commit_output" | grep -qE "(files were modified by this hook|reformatted|Fixing )"; then
            ((retry_count++))
            log "INFO" "Pre-commit hooks modified files, re-staging (attempt $retry_count)..."
            sleep 1
            continue
        fi

        log "ERROR" "Failed to create commit (non-recoverable error)"
        echo "$commit_output" | tail -30
        return 1
    done

    log "ERROR" "Failed to create commit after $max_retries retries"
    return 1
}

