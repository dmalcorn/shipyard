#!/bin/bash
#
# code-review-analysis-fix - Analyze Code Review Results and Apply Fixes
#
# Seven-phase workflow:
#   Phase 0 (REVIEW):    Run both code review scripts (BMAD + Claude in parallel)
#   Phase 1 (ANALYZE):   Compare BMAD and Claude code review results, create fix plan
#   Phase 2 (FIX):       Apply clear, non-controversial fixes from the plan
#   Phase 3 (ARCHITECT): Generate architect recommendations for controversial items
#   Phase 4 (ARCH-FIX):  Apply fixes from architect's fix plan
#   Phase 5 (CI):        Run CI pipeline (scripts/local_ci.sh) with auto-fix retry loop
#   Phase 6 (GIT):       Create local git commit with pre-commit hook handling
#
# Input files (from code-review-loop scripts, or auto-generated by review phase):
#   - epic<N>-code-review-bmad-results.log
#   - epic<N>-code-review-claude-results.log
#
# Output files:
#   - epic<N>-code-review-analysis.md       (analysis with agent comparison table)
#   - epic<N>-code-review-fix-plan.md        (clear fixes only - Phase 2 input)
#   - epic<N>-code-review-fix-plan-done.md  (execution log with timestamps - Phase 2 output)
#   - epic<N>-architect-review-needed.md    (controversial items + skipped items - Phase 3 input)
#   - epic<N>-architect-recommendations.md   (architect decisions - Phase 3 output)
#   - epic<N>-architect-fix-plan.md          (actionable fixes from architect - Phase 3 output)
#   - epic<N>-architect-fix-plan-done.md    (execution log - Phase 4 output)
#
# Usage: ./looper/code-review-analysis-fix.sh <epic-number> [phase] [--force]
#   phase: review, analyze, fix, architect, arch-fix, ci, git, or all (default: all)
#   --force: Skip architect review gate (not recommended)
#
# Recommended Workflow:
#   0. Run 'review' phase to execute both code review scripts (or run them manually)
#   1. Run 'analyze' phase to create fix plan + architect review needed
#   2. Run 'fix' phase to apply clear fixes immediately
#   3. Run 'architect' phase to get recommendations + fix plan for controversial items
#   4. Run 'arch-fix' phase to apply architect's fixes
#   5. Run 'ci' phase to run CI with auto-fix
#   6. Run 'git' phase to create local commit
#
# Examples:
#   ./looper/code-review-analysis-fix.sh 2              # Run all phases (including review)
#   ./looper/code-review-analysis-fix.sh 2 review       # Only run code review scripts
#   ./looper/code-review-analysis-fix.sh 2 analyze      # Only analyze (create plans)
#   ./looper/code-review-analysis-fix.sh 2 fix          # Only fix clear items
#   ./looper/code-review-analysis-fix.sh 2 architect    # Only get architect recommendations
#   ./looper/code-review-analysis-fix.sh 2 ci           # Only run CI
#   ./looper/code-review-analysis-fix.sh 2 git          # Only create commit
#   ./looper/code-review-analysis-fix.sh 2 all --force  # Run all, skip gates
#
# Stop: Ctrl+C (graceful shutdown)
#
# Prerequisites:
#   - Claude Code CLI installed
#   - Code review result files from both scripts
#

set -e
set -o pipefail

# Allow running from within a Claude Code session (e.g. via /bmad-dev)
unset CLAUDECODE 2>/dev/null || true

# =============================================================================
