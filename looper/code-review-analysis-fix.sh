# =============================================================================
# Logging function
# =============================================================================
log() {
    local level="$1"
    local message="$2"
    local timestamp=$(get_timestamp)

    case "$level" in
        "INFO")    echo -e "${CYAN}${timestamp} [${level}] ${message}${NC}" ;;
        "WARN")    echo -e "${YELLOW}${timestamp} [${level}] ${message}${NC}" ;;
        "ERROR")   echo -e "${RED}${timestamp} [${level}] ${message}${NC}" ;;
        "SUCCESS") echo -e "${GREEN}${timestamp} [${level}] ${message}${NC}" ;;
        "PHASE")   echo -e "${MAGENTA}${timestamp} [${level}] ${message}${NC}" ;;
        *)         echo -e "${timestamp} [${level}] ${message}" ;;
    esac

    echo "${timestamp} [${level}] ${message}" >> "$LOG_FILE"
}

# =============================================================================
# Duration formatting function
# =============================================================================
format_duration() {
    local seconds="$1"
    if [[ $seconds -ge 60 ]]; then
        local mins=$((seconds / 60))
        local secs=$((seconds % 60))
        echo "${mins}m ${secs}s"
    else
        echo "${seconds}s"
    fi
}

# =============================================================================
# Output preview function
# =============================================================================
log_output_preview() {
    local phase="$1"
    local output="$2"
    local lines="${3:-10}"

    local preview=$(echo "$output" | head -n "$lines")

    echo ""
    echo -e "${CYAN}--- OUTPUT PREVIEW: $phase (first $lines lines) ---${NC}"
    echo "$preview"
    echo -e "${CYAN}--- END PREVIEW ---${NC}"
    echo ""
}

# =============================================================================
# State Management
# =============================================================================
save_state() {
    local phase="$1"
    local status="$2"

    cat > "$STATE_FILE" << EOF
{
    "epic": $EPIC_NUMBER,
    "phase": "$phase",
    "status": "$status",
    "timestamp": "$(get_timestamp)"
}
EOF
}

# =============================================================================
# Phase ordering and resume logic
# =============================================================================
# Ordered list of phases (index = execution order)
PHASE_ORDER=("review" "analyze" "fix" "architect" "arch-fix" "ci" "git")

phase_to_index() {
    local target="$1"
    for i in "${!PHASE_ORDER[@]}"; do
        if [[ "${PHASE_ORDER[$i]}" == "$target" ]]; then
            echo "$i"
            return
        fi
    done
    echo "-1"
}

# Read state file and resolve which phase to resume from.
# Returns the phase name to start at (via stdout).
resolve_resume_phase() {
    if [[ ! -f "$STATE_FILE" ]]; then
        echo "review"
        return
    fi

    # Parse JSON (simple — single-level flat structure)
    local last_phase last_status
    last_phase=$(grep '"phase"' "$STATE_FILE" | sed 's/.*: *"\([^"]*\)".*/\1/')
    last_status=$(grep '"status"' "$STATE_FILE" | sed 's/.*: *"\([^"]*\)".*/\1/')

    local idx
    idx=$(phase_to_index "$last_phase")
    if [[ "$idx" == "-1" ]]; then
        echo "review"
        return
    fi

    case "$last_status" in
        complete|skipped)
            # Completed or skipped — advance to next phase
            local next=$(( idx + 1 ))
            if [[ $next -ge ${#PHASE_ORDER[@]} ]]; then
                # All phases done — restart from review
                echo "review"
            else
                echo "${PHASE_ORDER[$next]}"
            fi
            ;;
        *)
            # in_progress, failed, or anything else — re-run this phase
            echo "$last_phase"
            ;;
    esac
}

