# =============================================================================
# Check for Skipped Items and Append to Architect Review File
# =============================================================================
check_skipped_items() {
    if [[ ! -f "$FIX_PLAN_DONE_LOG" ]]; then
        return 0
    fi

    local skipped_count
    skipped_count=$(grep -cE "^\[.*SKIPPED #[0-9]+\]" "$FIX_PLAN_DONE_LOG" 2>/dev/null) || skipped_count=0

    if [[ $skipped_count -eq 0 ]]; then
        return 0
    fi

    log "WARN" "$skipped_count items were skipped during fix phase"
    log "INFO" "Appending skipped items to architect review file..."

    local existing_count=0
    if [[ -f "$ARCHITECT_REVIEW_FILE" ]]; then
        existing_count=$(grep -cE "^### [0-9]+\." "$ARCHITECT_REVIEW_FILE" 2>/dev/null) || existing_count=0
    else
        local timestamp=$(get_timestamp)
        cat > "$ARCHITECT_REVIEW_FILE" << EOF
# Epic $EPIC_NUMBER - Architect Review Required

**Generated:** $timestamp
**Source:** Code Review Analysis

---

## Items Requiring Architectural Decision

EOF
    fi

    local timestamp=$(get_timestamp)
    cat >> "$ARCHITECT_REVIEW_FILE" << EOF

---

## Items Skipped During Fix Phase

*Added: $timestamp*

EOF

    local item_num=$((existing_count + 1))
    grep -E "^\[.*SKIPPED #[0-9]+\]" "$FIX_PLAN_DONE_LOG" | while read -r line; do
        local skip_info=$(echo "$line" | sed 's/^\[.*SKIPPED #[0-9]*\] //')
        local skip_ref=$(echo "$line" | grep -oE "SKIPPED #[0-9]+")

        cat >> "$ARCHITECT_REVIEW_FILE" << EOF
### $item_num. $skip_ref
- **Source:** Fix phase execution
- **Reason:** $skip_info
- **Decision Required:** Determine the correct fix approach

EOF
        item_num=$((item_num + 1))
    done

    log "WARN" "Appended $skipped_count skipped items to: $ARCHITECT_REVIEW_FILE"
}

