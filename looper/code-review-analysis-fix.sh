# =============================================================================
# Graceful shutdown handler
# =============================================================================
# Global variables to track processes for cleanup
CLAUDE_PID=""
REVIEW_PIDS=""  # Space-separated PIDs for parallel review scripts

shutdown() {
    echo ""
    log "INFO" "Shutting down Analysis & Fix Loop..."

    # Kill any running parallel review scripts
    if [[ -n "$REVIEW_PIDS" ]]; then
        log "INFO" "Terminating review script processes: $REVIEW_PIDS"
        for pid in $REVIEW_PIDS; do
            kill -TERM "$pid" 2>/dev/null || true
        done
        sleep 1
        for pid in $REVIEW_PIDS; do
            kill -KILL "$pid" 2>/dev/null || true
        done
    fi

    # Kill any running Claude process
    if [[ -n "$CLAUDE_PID" ]]; then
        log "INFO" "Terminating Claude process (PID: $CLAUDE_PID)..."
        kill -TERM "$CLAUDE_PID" 2>/dev/null || true
        sleep 1
        kill -KILL "$CLAUDE_PID" 2>/dev/null || true
    fi

    # Clean up any temp files
    rm -f /tmp/claude_output_$$.txt /tmp/claude_prompt_$$.txt 2>/dev/null || true

    log "INFO" "State saved to: $STATE_FILE"
    log "INFO" "Log saved to: $LOG_FILE"
    exit 0
}

trap shutdown SIGINT SIGTERM

# =============================================================================
# Run Claude with proper signal handling
# =============================================================================
# This function runs Claude in its own process group so Ctrl+C works properly.
# Usage: run_claude_with_signal_handling "prompt" "allowed_tools" [timeout]
# Returns: Sets global CLAUDE_RESULT and CLAUDE_EXIT_CODE
#          Exit code 124 indicates timeout (matching standard timeout command)
CLAUDE_RESULT=""
CLAUDE_EXIT_CODE=0

run_claude_with_signal_handling() {
    local prompt="$1"
    local allowed_tools="$2"
    local timeout_duration="${3:-}"  # Optional timeout (e.g., "35m")

    local output_file="/tmp/claude_output_$$.txt"
    local prompt_file="/tmp/claude_prompt_$$.txt"

    # Clean up any previous temp files
    rm -f "$output_file" "$prompt_file" 2>/dev/null || true

    # Write prompt to temp file to avoid shell escaping issues
    printf '%s' "$prompt" > "$prompt_file"

    # Run Claude in background (no setsid — not available on Windows/MINGW)
    bash -c '
        exec claude --print --allowedTools "'"$allowed_tools"'" -- "$(cat "'"$prompt_file"'")"
    ' </dev/null >"$output_file" 2>&1 &
    CLAUDE_PID=$!

    # Wait for the process to complete, with optional timeout
    if [[ -n "$timeout_duration" ]]; then
        # Manual timeout: poll in a loop
        local timeout_secs=0
        # Parse timeout duration (supports "35m" or "300" seconds)
        if [[ "$timeout_duration" =~ ^([0-9]+)m$ ]]; then
            timeout_secs=$(( ${BASH_REMATCH[1]} * 60 ))
        elif [[ "$timeout_duration" =~ ^[0-9]+$ ]]; then
            timeout_secs=$timeout_duration
        else
            timeout_secs=2100  # Default 35 min
        fi

        local elapsed=0
        while kill -0 $CLAUDE_PID 2>/dev/null; do
            sleep 5
            elapsed=$((elapsed + 5))
            if [[ $elapsed -ge $timeout_secs ]]; then
                log "WARN" "Claude timed out after $timeout_duration - terminating..."
                kill -TERM $CLAUDE_PID 2>/dev/null || true
                sleep 2
                kill -KILL $CLAUDE_PID 2>/dev/null || true
                CLAUDE_EXIT_CODE=124
                CLAUDE_RESULT=$(cat "$output_file" 2>/dev/null || echo "")
                rm -f "$output_file" "$prompt_file" 2>/dev/null || true
                CLAUDE_PID=""
                return $CLAUDE_EXIT_CODE
            fi
        done
        wait $CLAUDE_PID 2>/dev/null
        CLAUDE_EXIT_CODE=$?
    else
        wait $CLAUDE_PID 2>/dev/null
        CLAUDE_EXIT_CODE=$?
    fi

    # Read the output
    CLAUDE_RESULT=$(cat "$output_file" 2>/dev/null || echo "")

    # Clean up
    rm -f "$output_file" "$prompt_file" 2>/dev/null || true
    CLAUDE_PID=""

    return $CLAUDE_EXIT_CODE
}

# =============================================================================
# Check if required input files exist
# =============================================================================
check_input_files() {
    local missing=0

    if [[ ! -f "$BMAD_RESULTS_FILE" ]]; then
        log "ERROR" "BMAD results file not found: $BMAD_RESULTS_FILE"
        log "INFO" "Run: ./looper/code-review-loop-bmad.sh $EPIC_NUMBER"
        missing=1
    else
        log "INFO" "Found BMAD results: $BMAD_RESULTS_FILE"
    fi

    if [[ ! -f "$CLAUDE_RESULTS_FILE" ]]; then
        log "ERROR" "Claude results file not found: $CLAUDE_RESULTS_FILE"
        log "INFO" "Run: ./looper/code-review-loop-claude.sh $EPIC_NUMBER"
        missing=1
    else
        log "INFO" "Found Claude results: $CLAUDE_RESULTS_FILE"
    fi

    return $missing
}

# =============================================================================
# PHASE 0: Run Both Code Review Scripts
# =============================================================================
run_review_phase() {
    local phase_start=$(date +%s)

    echo ""
    echo -e "${MAGENTA}============================================================${NC}"
    log "PHASE" "PHASE 0: RUN CODE REVIEW SCRIPTS"
    echo -e "${MAGENTA}============================================================${NC}"
    echo ""

    # Check which result files already exist
    local need_bmad=true
    local need_claude=true

    if [[ -f "$BMAD_RESULTS_FILE" ]]; then
        if [[ "$FORCE_FIX" == "true" ]]; then
            log "WARN" "--force specified, re-running BMAD review despite existing results"
        else
            log "INFO" "BMAD results already exist — skipping: $BMAD_RESULTS_FILE"
            need_bmad=false
        fi
    fi

    if [[ -f "$CLAUDE_RESULTS_FILE" ]]; then
        if [[ "$FORCE_FIX" == "true" ]]; then
            log "WARN" "--force specified, re-running Claude review despite existing results"
        else
            log "INFO" "Claude results already exist — skipping: $CLAUDE_RESULTS_FILE"
            need_claude=false
        fi
    fi

    # If both exist and not forcing, skip entire phase
    if [[ "$need_bmad" == "false" && "$need_claude" == "false" ]]; then
        log "INFO" "Both review result files already exist — skipping review phase"
        log "INFO" "  (Delete result files to force re-review, or use --force)"
        save_state "review" "complete"
        return 0
    fi

    save_state "review" "in_progress"

    local bmad_script="$LOOPER_DIR/code-review-loop-bmad.sh"
    local claude_script="$LOOPER_DIR/code-review-loop-claude.sh"

    # Verify needed scripts exist before launching anything
    if [[ "$need_bmad" == "true" && ! -f "$bmad_script" ]]; then
        log "ERROR" "BMAD review script not found: $bmad_script"
        return 1
    fi
    if [[ "$need_claude" == "true" && ! -f "$claude_script" ]]; then
        log "ERROR" "Claude review script not found: $claude_script"
        return 1
    fi
    [[ "$need_bmad" == "true" ]] && chmod +x "$bmad_script" 2>/dev/null || true
    [[ "$need_claude" == "true" ]] && chmod +x "$claude_script" 2>/dev/null || true

    if [[ "$need_bmad" == "true" && "$need_claude" == "true" ]]; then
        # ---- PARALLEL: Both reviews needed ----
        log "INFO" "Running BOTH review scripts in parallel..."
        log "INFO" "  BMAD script:   $bmad_script"
        log "INFO" "  Claude script: $claude_script"
        log "INFO" "  Epic: $EPIC_NUMBER"
        echo ""

        local parallel_start=$(date +%s)

        "$bmad_script" "$EPIC_NUMBER" &
        local bmad_pid=$!

        "$claude_script" "$EPIC_NUMBER" &
        local claude_pid=$!

        REVIEW_PIDS="$bmad_pid $claude_pid"

        log "INFO" "BMAD review launched  (PID: $bmad_pid)"
        log "INFO" "Claude review launched (PID: $claude_pid)"
        log "INFO" "Waiting for both reviews to complete..."

        local bmad_exit=0
        local claude_exit=0
        wait "$bmad_pid" || bmad_exit=$?
        wait "$claude_pid" || claude_exit=$?

        REVIEW_PIDS=""

        local parallel_duration=$(format_duration $(($(date +%s) - parallel_start)))

        if [[ $bmad_exit -ne 0 ]]; then
            log "ERROR" "BMAD review script failed (exit code: $bmad_exit)"
        elif [[ ! -f "$BMAD_RESULTS_FILE" ]]; then
            log "ERROR" "BMAD results file was not created: $BMAD_RESULTS_FILE"
            bmad_exit=1
        else
            log "SUCCESS" "BMAD review complete"
        fi

        if [[ $claude_exit -ne 0 ]]; then
            log "ERROR" "Claude review script failed (exit code: $claude_exit)"
        elif [[ ! -f "$CLAUDE_RESULTS_FILE" ]]; then
            log "ERROR" "Claude results file was not created: $CLAUDE_RESULTS_FILE"
            claude_exit=1
        else
            log "SUCCESS" "Claude review complete"
        fi

        log "INFO" "Parallel review duration: $parallel_duration"

        if [[ $bmad_exit -ne 0 || $claude_exit -ne 0 ]]; then
            save_state "review" "failed"
            return 1
        fi

    elif [[ "$need_bmad" == "true" ]]; then
        log "INFO" "Running BMAD code review script..."
        local bmad_exit_code=0
        if "$bmad_script" "$EPIC_NUMBER"; then
            log "SUCCESS" "BMAD review complete"
        else
            bmad_exit_code=$?
            log "ERROR" "BMAD review script failed (exit code: $bmad_exit_code)"
            save_state "review" "failed_bmad"
            return 1
        fi
    else
        log "INFO" "Running Claude code review script..."
        local claude_exit_code=0
        if "$claude_script" "$EPIC_NUMBER"; then
            log "SUCCESS" "Claude review complete"
        else
            claude_exit_code=$?
            log "ERROR" "Claude review script failed (exit code: $claude_exit_code)"
            save_state "review" "failed_claude"
            return 1
        fi
    fi

    local phase_duration=$(format_duration $(($(date +%s) - phase_start)))
    log "SUCCESS" "Phase 0 (Review) complete - Duration: $phase_duration"
    save_state "review" "complete"

    return 0
}

# =============================================================================
# PHASE 1: Analyze and Compare Code Review Results
# =============================================================================
run_analysis_phase() {
    local phase_start=$(date +%s)

    echo ""
    echo -e "${MAGENTA}============================================================${NC}"
    log "PHASE" "PHASE 1: ANALYZE CODE REVIEW RESULTS"
    echo -e "${MAGENTA}============================================================${NC}"
    echo ""

    save_state "analyze" "in_progress"

    if ! check_input_files; then
        log "ERROR" "Missing required input files. Run code review scripts first."
        return 1
    fi

    log "INFO" "Analyzing and comparing code review results..."
    log "INFO" "  Allowed tools: $TOOLS_ANALYZE"

    local prompt="You are analyzing code review results from two different review agents for Epic $EPIC_NUMBER.

TASK: Compare and analyze the code review findings from both agents, then create:
1. A fix plan with ONLY clear, non-controversial fixes
2. An architect review file with ONLY controversial/architectural items

CRITICAL: Items must go to ONE place only - NO OVERLAP between fix plan and architect review.

INPUT FILES TO READ:
1. BMAD Agent Results: $BMAD_RESULTS_FILE
2. Claude Direct Results: $CLAUDE_RESULTS_FILE
3. Project Coding Rules: $PROJECT_ROOT/CLAUDE.md
4. Coding Standards: $PROJECT_ROOT/coding-standards.md

## ISSUE CLASSIFICATION RULES

### Category A: CLEAR FIXES (go to Fix Plan ONLY)
- The fix is obvious and unambiguous
- No architectural decision is needed
- Examples: typos, missing imports, wrong paths, test assertion fixes, lint violations

### Category B: ARCHITECT REVIEW (go to Architect Review ONLY)
- Multiple valid approaches exist
- Security implications need expert review
- API contract changes are involved
- The fix could affect other epics/stories
- Examples: agent communication strategy, state management decisions, tool permission scope

IMPORTANT: If you're unsure which category, put it in Category B.

## REQUIRED OUTPUTS

### Output 1: Analysis File ($ANALYSIS_FILE)
Detailed analysis with: executive summary, agent coverage table, issue comparison, unique findings, consolidated issue list with A/B classification.

### Output 2: Fix Plan File ($FIX_PLAN_FILE)
ONLY Category A issues. Structured by priority (P0-P3) with file path, line number, issue description, specific fix action, and verification method.

### Output 3: Architect Review File ($ARCHITECT_REVIEW_FILE)
ONLY Category B issues. Each item needs: file paths, current state, why it needs architect review, options table, recommendation, and specific decision question.

Begin by reading the input files now."

    local timestamp=$(get_timestamp)
    cat >> "$LOG_FILE" << EOF

================================================================================
PROMPT: Phase 1 Analysis | Time: $timestamp
================================================================================
$prompt
================================================================================

EOF

    echo -e "${YELLOW}>>> Invoking Claude for analysis...${NC}"
    echo -e "${CYAN}    (Press Ctrl+C to stop gracefully)${NC}"
    run_claude_with_signal_handling "$prompt" "$TOOLS_ANALYZE"
    local result="$CLAUDE_RESULT"
    local exit_code=$CLAUDE_EXIT_CODE

    cat >> "$LOG_FILE" << EOF

================================================================================
OUTPUT: Phase 1 Analysis
================================================================================
$result
================================================================================

EOF

    log_output_preview "Phase 1 Analysis" "$result" 15

    if [[ $exit_code -ne 0 ]]; then
        log "ERROR" "Analysis phase failed (exit code: $exit_code)"
        save_state "analyze" "failed"
        return 1
    fi

    # Verify output files were created
    if [[ -f "$ANALYSIS_FILE" ]]; then
        log "SUCCESS" "Analysis file created: $ANALYSIS_FILE"
    else
        log "WARN" "Analysis file was not created: $ANALYSIS_FILE"
    fi

    if [[ -f "$FIX_PLAN_FILE" ]]; then
        log "SUCCESS" "Fix plan created: $FIX_PLAN_FILE"
    else
        log "WARN" "Fix plan file was not created: $FIX_PLAN_FILE"
    fi

    if [[ -f "$ARCHITECT_REVIEW_FILE" ]]; then
        local arch_items
        arch_items=$(grep -cE "^### [0-9]+\." "$ARCHITECT_REVIEW_FILE" 2>/dev/null) || arch_items=0
        if [[ $arch_items -gt 0 ]]; then
            log "SUCCESS" "Architect review created: $ARCHITECT_REVIEW_FILE ($arch_items items)"
        fi
    fi

    local phase_duration=$(format_duration $(($(date +%s) - phase_start)))
    log "SUCCESS" "Phase 1 (Analysis) complete - Duration: $phase_duration"
    save_state "analyze" "complete"

    return 0
}

# =============================================================================
# Check for Architect Review Gate
# =============================================================================
check_architect_review_gate() {
    if [[ ! -f "$ARCHITECT_REVIEW_FILE" ]]; then
        return 0
    fi

    local review_count
    review_count=$(grep -cE "^### [0-9]+\." "$ARCHITECT_REVIEW_FILE" 2>/dev/null) || review_count=0

    if [[ $review_count -eq 0 ]]; then
        return 0
    fi

    echo ""
    log "WARN" "Found $review_count items requiring architect review"
    log "INFO" "Architect review file: $ARCHITECT_REVIEW_FILE"

    if [[ "${FORCE_FIX:-false}" == "true" ]]; then
        log "WARN" "FORCE_FIX=true - Continuing without architect review"
        return 0
    fi

    read -p "Continue without architect review? [y/N]: " choice
    case "$choice" in
        y|Y) log "WARN" "Continuing - architect items will be skipped"; return 0 ;;
        *)   log "INFO" "Review: $ARCHITECT_REVIEW_FILE then run: $0 $EPIC_NUMBER fix"; exit 0 ;;
    esac
}

# =============================================================================
# PHASE 2: Apply Fixes from the Plan
# =============================================================================
