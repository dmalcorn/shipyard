# Main Entry Point
# =============================================================================
main() {
    echo ""
    echo -e "  ${MAGENTA}=====================================${NC}"
    echo -e "  ${MAGENTA}CODE REVIEW ANALYSIS & FIX${NC}"
    echo -e "  ${MAGENTA}Shipyard Automated Code Quality${NC}"
    echo -e "  ${MAGENTA}=====================================${NC}"
    echo ""

    if [[ -z "$EPIC_NUMBER" ]]; then
        echo "Usage: $0 <epic-number> [phase] [--force]"
        echo ""
        echo "Phases:"
        echo "  (none)    - Auto-resume from state file, or run all if no state"
        echo "  review    - Run both code review scripts (BMAD + Claude)"
        echo "  analyze   - Create fix plan (clear fixes) + architect review (controversial)"
        echo "  fix       - Apply clear, non-controversial fixes"
        echo "  architect - Generate architect recommendations + fix plan"
        echo "  arch-fix  - Apply fixes from the architect's fix plan"
        echo "  ci        - Run CI pipeline with auto-fix retry loop"
        echo "  git       - Create local git commit"
        echo "  all       - Run all seven phases from the beginning"
        echo ""
        echo "Options:"
        echo "  --force - Skip architect review gate"
        echo ""
        echo "Examples:"
        echo "  $0 2              # Auto-resume from last state (or all if fresh)"
        echo "  $0 2 all          # Force run all phases from the start"
        echo "  $0 2 review       # Only run code review scripts"
        echo "  $0 2 analyze      # Only analyze"
        echo "  $0 2 fix          # Only fix clear items"
        echo "  $0 2 ci           # Only run CI"
        echo "  $0 2 git          # Only create commit"
        exit 1
    fi

    # Resolve phase: empty → auto-resume, explicit → validate
    if [[ -z "$PHASE" ]]; then
        PHASE="resume"
    fi

    case "$PHASE" in
        review|analyze|fix|architect|arch-fix|ci|git|all|resume) ;;
        *) echo -e "${RED}Error: Invalid phase '$PHASE'${NC}"; exit 1 ;;
    esac

    # Resolve run mode:
    #   resume / empty → read state file, run from that phase through the end
    #   all            → run all phases from the beginning
    #   <specific>     → run only that one phase (original behavior)
    ORIGINAL_PHASE="$PHASE"
    if [[ "$PHASE" == "resume" ]]; then
        local resume_from
        resume_from=$(resolve_resume_phase)
        log "INFO" "Resuming from state file → starting at phase: $resume_from"
        START_PHASE_INDEX=$(phase_to_index "$resume_from")
    elif [[ "$PHASE" == "all" ]]; then
        START_PHASE_INDEX=0
    else
        # Single-phase mode: only run that one phase
        START_PHASE_INDEX=-1  # sentinel
    fi

    local phase_label
    if [[ $START_PHASE_INDEX -ge 0 ]]; then
        phase_label="${PHASE_ORDER[$START_PHASE_INDEX]} → git"
    else
        phase_label="$PHASE (single)"
    fi

    cat > "$LOG_FILE" << EOF
================================================================================
CODE REVIEW ANALYSIS & FIX LOG
Epic: $EPIC_NUMBER
Phase: $phase_label
Started: $(get_timestamp)
================================================================================

EOF

    check_dependencies

    log "INFO" "Starting Code Review Analysis & Fix - Epic $EPIC_NUMBER, phases: $phase_label"
    echo ""

    local total_start=$(date +%s)

    # Helper: should this phase run?
    #   Multi-phase mode (START_PHASE_INDEX >= 0): run if index >= start
    #   Single-phase mode (START_PHASE_INDEX == -1): run if name matches ORIGINAL_PHASE
    should_run() {
        local phase_name="$1"
        if [[ $START_PHASE_INDEX -ge 0 ]]; then
            local idx
            idx=$(phase_to_index "$phase_name")
            [[ $idx -ge $START_PHASE_INDEX ]]
        else
            [[ "$phase_name" == "$ORIGINAL_PHASE" ]]
        fi
    }

    # Track results for phases that actually ran
    declare -A phase_result  # "true", "false", or unset (not run)
    local failed=false
    local sleep_between=15   # seconds between phases in multi-phase mode
    local multi_phase=$(( START_PHASE_INDEX >= 0 ? 1 : 0 ))

    # Phase 0: Review
    if should_run "review"; then
        if run_review_phase; then
            phase_result[review]=true
        else
            phase_result[review]=false
            failed=true
            [[ $multi_phase -eq 1 ]] && { log "ERROR" "Review failed — stopping pipeline."; }
        fi
        [[ $multi_phase -eq 1 && "$failed" == "false" ]] && sleep 30
    fi

    # Phase 1: Analysis
    if should_run "analyze" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        if run_analysis_phase; then
            phase_result[analyze]=true
        else
            phase_result[analyze]=false
            failed=true
        fi
        [[ $multi_phase -eq 1 && "$failed" == "false" ]] && sleep $sleep_between
    fi

    # Phase 2: Fix
    if should_run "fix" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        [[ $multi_phase -eq 0 ]] && { check_architect_review_gate || true; }
        if run_fix_phase; then
            phase_result[fix]=true
        else
            phase_result[fix]=false
            # Fix failure is non-fatal for the pipeline — architect + CI can still run
        fi
        [[ $multi_phase -eq 1 ]] && sleep $sleep_between
    fi

    # Phase 3: Architect
    if should_run "architect" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        if run_architect_phase; then
            phase_result[architect]=true
        else
            phase_result[architect]=false
            failed=true
        fi
        [[ $multi_phase -eq 1 && "$failed" == "false" ]] && sleep $sleep_between
    fi

    # Phase 4: Arch-Fix
    if should_run "arch-fix" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        if run_arch_fix_phase; then
            phase_result[arch-fix]=true
        else
            phase_result[arch-fix]=false
            failed=true
        fi
        [[ $multi_phase -eq 1 && "$failed" == "false" ]] && sleep $sleep_between
    fi

    # Phase 5: CI
    if should_run "ci" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        if run_ci_phase; then
            phase_result[ci]=true
        else
            phase_result[ci]=false
            failed=true
        fi
        [[ $multi_phase -eq 1 && "$failed" == "false" ]] && sleep 5
    fi

    # Phase 6: Git
    if should_run "git" && [[ "$failed" == "false" || $multi_phase -eq 0 ]]; then
        if run_git_phase; then
            phase_result[git]=true
        else
            phase_result[git]=false
            failed=true
        fi
    fi

    # Final Summary — only report phases that were attempted
    local total_duration=$(format_duration $(($(date +%s) - total_start)))
    local phase_names=("review" "analyze" "fix" "architect" "arch-fix" "ci" "git")
    local phase_labels=("Phase 0 (Review)" "Phase 1 (Analysis)" "Phase 2 (Fix)" "Phase 3 (Architect)" "Phase 4 (Arch-Fix)" "Phase 5 (CI)" "Phase 6 (Git)")

    echo ""
    echo -e "${MAGENTA}============================================================${NC}"
    log "INFO" "CODE REVIEW ANALYSIS & FIX COMPLETE"
    echo -e "${MAGENTA}============================================================${NC}"
    log "INFO" "Epic: $EPIC_NUMBER | Duration: $total_duration"

    local any_failed=false
    for i in "${!phase_names[@]}"; do
        local pn="${phase_names[$i]}"
        local pl="${phase_labels[$i]}"
        if [[ -n "${phase_result[$pn]+x}" ]]; then
            if [[ "${phase_result[$pn]}" == "true" ]]; then
                log "SUCCESS" "$pl: PASSED"
            else
                log "ERROR" "$pl: FAILED"
                any_failed=true
            fi
        fi
    done

    log "INFO" "Full log: $LOG_FILE"
    echo -e "${MAGENTA}============================================================${NC}"

    if [[ "$any_failed" == "true" ]]; then
        exit 1
    else
        exit 0
    fi
}

main "$@"
